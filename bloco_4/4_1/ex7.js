switch (true) {
    case (p >= 90):
        console.log('A');
        break;
    case (p >= 80):
        console.log('B');
        break;
    case (p >= 70):
        console.log('C');
        break;
    case (p >= 60):
        console.log('D');
        break;
    case (p >= 50):
        console.log('E');
        break;
    default:
        console.log('F');
}